import { CHRONOLOGICAL_ORDER } from "./chronological-order";

export interface SurahData {
  id: number;
  name: string;
  nameTransliteration: string;
  nameTranslation: string;
  revelationType: "Meccan" | "Medinan";
  chronologicalOrder: number;
  versesCount: number;
  description?: string;
}

// Get chronological data for a surah
function getChronologicalData(surahNumber: number) {
  const data = CHRONOLOGICAL_ORDER.find((s) => s.surahNumber === surahNumber);
  return {
    chronologicalOrder: data?.chronologicalOrder ?? surahNumber,
    revelationType: data?.revelationType ?? "Meccan",
  };
}

